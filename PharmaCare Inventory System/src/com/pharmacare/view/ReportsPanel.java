    private void btnLowStockReportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLowStockReportActionPerformed
        try {
            java.io.InputStream reportStream = getClass().getResourceAsStream("/reports/rpt_low_stock.jrxml");
            if (reportStream == null) {
                JOptionPane.showMessageDialog(this, "Report file not found.", "Resource Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            // 1. Report එක compile කරලා, data වලින් fill කරනවා
            net.sf.jasperreports.engine.JasperReport jr = net.sf.jasperreports.engine.JasperCompileManager.compileReport(reportStream);
            net.sf.jasperreports.engine.JasperPrint jp = net.sf.jasperreports.engine.JasperFillManager.fillReport(jr, null, DBConnection.getInstance().getConnection());

            // 2. --- මෙන්න වැදගත්ම කොටස: Report එකේ පිටු තියෙනවද කියලා බලනවා ---
            if (jp.getPages() != null && !jp.getPages().isEmpty()) {
                // 3A. පිටු තියෙනවා නම්, Viewer එක පෙන්නනවා
                net.sf.jasperreports.view.JasperViewer.viewReport(jp, false);
            } else {
                // 3B. පිටු නැත්නම්, අපේම message එකක් පෙන්නනවා
                JOptionPane.showMessageDialog(this, "No low stock items found to report.", "Report Information", JOptionPane.INFORMATION_MESSAGE);
            }

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error generating report: " + e.getMessage(), "Report Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnLowStockReportActionPerformed


